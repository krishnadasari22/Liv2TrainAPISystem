package com.televisory.capitalmarket.model;

import java.util.List;

public class JasperDynamicModel {

	List<String> slots;

	public JasperDynamicModel() {
	}
	public JasperDynamicModel(List<String> slots) {
		this.slots = slots;
	}

	public List<String> getSlots() {
		return slots;
	}

	public void setSlots(List<String> slots) {
		this.slots = slots;
	}



}
